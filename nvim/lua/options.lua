-- ==========================================================================
-- 기본 에디터 옵션 설정 (options.lua)
-- ==========================================================================
-- Neovim의 기본 동작을 설정하는 파일입니다.
-- 기존 .vimrc에서 유용한 설정을 Lua 문법으로 이식했습니다.
-- ==========================================================================

local opt = vim.opt  -- vim.opt는 Vim의 'set' 명령과 동일한 역할

-- --------------------------------------------------------------------------
-- 줄 번호 표시
-- --------------------------------------------------------------------------
opt.number = true          -- 줄 번호 표시 (set nu)
opt.relativenumber = true  -- 상대 줄 번호 표시 (현재 줄로부터의 거리)
                           -- 예: 3줄 위는 '3', 현재 줄은 절대 번호 표시
                           -- j/k 이동 시 몇 줄 이동해야 하는지 바로 알 수 있음

-- --------------------------------------------------------------------------
-- 들여쓰기 설정 (탭 = 4 스페이스)
-- --------------------------------------------------------------------------
opt.tabstop = 4        -- 탭 문자의 화면 표시 너비 = 4칸
opt.shiftwidth = 4     -- >> 또는 << 자동 들여쓰기 시 4칸 이동
opt.expandtab = true   -- 탭 키 누르면 스페이스로 변환 (실제 탭 문자 대신)
opt.autoindent = true  -- 새 줄에서 이전 줄의 들여쓰기를 그대로 유지
opt.cindent = true     -- C/C++ 스타일 자동 들여쓰기 (smartindent와 충돌하므로 cindent만 사용)

-- --------------------------------------------------------------------------
-- 인코딩 설정 (한글 지원)
-- --------------------------------------------------------------------------
opt.encoding = "utf-8"                    -- 내부 인코딩을 UTF-8로 설정
opt.fileencodings = "ucs-bom,utf-8,euc-kr,latin1"  -- 파일 열 때 인코딩 감지 순서
                                          -- UTF-8 → EUC-KR(한글) → Latin1 순으로 시도

-- --------------------------------------------------------------------------
-- 검색 설정
-- --------------------------------------------------------------------------
opt.hlsearch = true    -- 검색 결과 하이라이트 (노란색으로 강조)
opt.incsearch = true   -- 검색어 입력 중에 실시간으로 매칭 표시
opt.ignorecase = true  -- 검색 시 대소문자 구분 안 함
opt.smartcase = true   -- 검색어에 대문자가 있으면 대소문자 구분함
                       -- 예: "hello" → 대소문자 무시, "Hello" → 정확히 매칭
opt.wrapscan = false   -- 파일 끝에서 검색 시 처음으로 돌아가지 않음

-- --------------------------------------------------------------------------
-- 화면 표시
-- --------------------------------------------------------------------------
opt.cursorline = true    -- 현재 줄 하이라이트 (배경색으로 강조)
opt.showmatch = true     -- 괄호 입력 시 매칭되는 괄호를 잠시 강조
opt.title = true         -- 터미널 타이틀에 파일명 표시
opt.ruler = true         -- 우측 하단에 커서 위치(줄:열) 표시
opt.signcolumn = "yes"   -- 왼쪽에 기호 열 항상 표시 (git 변경, LSP 에러 등)
opt.termguicolors = true -- 24비트 트루컬러 지원 (모던 컬러스킴에 필요)
opt.laststatus = 2       -- 상태바 항상 표시
opt.linebreak = true     -- 단어 단위로 줄바꿈 (단어 중간에서 끊지 않음)
opt.scrolloff = 8        -- 커서가 화면 가장자리에서 8줄 떨어지면 스크롤 시작
                          -- 항상 위아래 맥락을 볼 수 있음

-- --------------------------------------------------------------------------
-- 파일/백업 설정
-- --------------------------------------------------------------------------
opt.backup = false      -- 백업 파일(~) 생성하지 않음
opt.swapfile = false    -- 스왑 파일(.swp) 생성하지 않음
opt.undofile = true     -- 실행 취소(undo) 기록을 파일로 저장
                        -- Neovim을 닫았다 열어도 undo 가능!

-- --------------------------------------------------------------------------
-- 마우스 / 클립보드
-- --------------------------------------------------------------------------
opt.mouse = "a"                   -- 모든 모드에서 마우스 사용 가능
opt.clipboard = "unnamedplus"     -- 시스템 클립보드와 공유
                                  -- 복사(yank)하면 시스템 클립보드에도 들어감

-- --------------------------------------------------------------------------
-- 백스페이스 동작
-- --------------------------------------------------------------------------
opt.backspace = "eol,start,indent"  -- 백스페이스로 줄 끝, 시작, 들여쓰기 삭제 가능

-- --------------------------------------------------------------------------
-- 기타 편의 설정
-- --------------------------------------------------------------------------
opt.history = 1000       -- 명령어 히스토리 1000개 저장
opt.foldenable = false   -- 코드 접기(folding) 비활성화
opt.splitbelow = true    -- 수평 분할 시 새 창이 아래에 열림
opt.splitright = true    -- 수직 분할 시 새 창이 오른쪽에 열림
opt.updatetime = 250     -- 커서 멈춘 후 250ms 뒤에 이벤트 발생 (기본값 4000ms)
                          -- git 변경 표시, LSP 진단 등이 더 빨리 업데이트됨
opt.timeoutlen = 300     -- 키 매핑 조합 대기 시간 (300ms)
opt.visualbell = true    -- 비프음 대신 화면 깜빡임

-- 후행 공백 하이라이트 끔 (CheckBot은 커밋 전에 별도 실행)
