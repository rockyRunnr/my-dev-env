-- ==========================================================================
-- 키 매핑 설정 (keymaps.lua)
-- ==========================================================================
-- 자주 쓰는 동작에 대한 단축키를 정의합니다.
-- 기존 .vimrc의 키 매핑을 유지하면서 새로운 키맵을 추가합니다.
--
-- vim.keymap.set(모드, 키, 동작, 옵션) 형태로 설정합니다.
--   모드: "n" = Normal, "i" = Insert, "v" = Visual, "x" = Visual Block
-- ==========================================================================

-- --------------------------------------------------------------------------
-- Leader 키 설정
-- --------------------------------------------------------------------------
-- Leader 키는 사용자 정의 단축키의 접두사 역할을 합니다.
-- 예: Leader가 Space이면 "Space ff"로 파일 검색 실행
vim.g.mapleader = " "       -- Leader 키를 Space로 설정
vim.g.maplocalleader = " "  -- 파일 타입별 로컬 Leader도 동일하게 설정

local map = vim.keymap.set  -- 타이핑 줄이기 위한 별칭
local opts = { noremap = true, silent = true }  -- 기본 옵션: 재귀 매핑 방지, 출력 숨김

-- --------------------------------------------------------------------------
-- 버퍼(파일) 이동 (기존 .vimrc와 동일)
-- --------------------------------------------------------------------------
-- 여러 파일을 열었을 때 버퍼 간 이동
map("n", "<leader>q", ":bprevious<CR>", { desc = "이전 버퍼로 이동", noremap = true, silent = true })
map("n", "<leader>w", ":bnext<CR>", { desc = "다음 버퍼로 이동", noremap = true, silent = true })
map("n", "<leader>x", ":bdelete<CR>", { desc = "현재 버퍼 닫기", noremap = true, silent = true })

-- --------------------------------------------------------------------------
-- 창(window) 이동
-- --------------------------------------------------------------------------
-- Ctrl + h/j/k/l 로 분할된 창 사이를 이동
-- 기본 Vim은 Ctrl+w 누른 뒤 h/j/k/l인데, 한 키 조합으로 단축
map("n", "<C-h>", "<C-w>h", { desc = "왼쪽 창으로 이동" })
map("n", "<C-j>", "<C-w>j", { desc = "아래쪽 창으로 이동" })
map("n", "<C-k>", "<C-w>k", { desc = "위쪽 창으로 이동" })
map("n", "<C-l>", "<C-w>l", { desc = "오른쪽 창으로 이동" })

-- --------------------------------------------------------------------------
-- 검색 하이라이트 끄기
-- --------------------------------------------------------------------------
-- 검색 후 하이라이트가 남아있을 때 ESC 두 번으로 제거
map("n", "<Esc><Esc>", ":nohlsearch<CR>", { desc = "검색 하이라이트 끄기", noremap = true, silent = true })

-- --------------------------------------------------------------------------
-- 줄 이동 비활성화
-- --------------------------------------------------------------------------
-- Alt+j/k 줄 이동 비활성화 (ESC+j 오입력으로 코드 줄 스왑 방지)
for _, mode in ipairs({ "n", "i", "v" }) do
  map(mode, "<A-j>", "<Nop>", { desc = "" })
  map(mode, "<A-k>", "<Nop>", { desc = "" })
end

-- --------------------------------------------------------------------------
-- 들여쓰기 유지
-- --------------------------------------------------------------------------
-- Visual 모드에서 들여쓰기 후 선택이 해제되지 않도록
map("v", "<", "<gv", { desc = "들여쓰기 줄이고 선택 유지" })
map("v", ">", ">gv", { desc = "들여쓰기 늘리고 선택 유지" })

-- --------------------------------------------------------------------------
-- 파일 탐색기 (nvim-tree) 토글
-- --------------------------------------------------------------------------
map("n", "<leader>e", ":NvimTreeToggle<CR>", { desc = "파일 탐색기 열기/닫기", noremap = true, silent = true })

-- --------------------------------------------------------------------------
-- Telescope (퍼지 검색) 키맵
-- --------------------------------------------------------------------------
-- 이 키맵들은 Telescope 플러그인이 설치된 후 동작합니다.
-- plugins.lua에서 lazy-loading 되므로 여기서는 선언만 합니다.
local telescope_ok, builtin = pcall(require, "telescope.builtin")
if telescope_ok then
  map("n", "<leader>ff", builtin.find_files, { desc = "파일 이름으로 검색" })
  map("n", "<leader>fg", builtin.live_grep, { desc = "파일 내용(텍스트)으로 검색" })
  map("n", "<leader>fb", builtin.buffers, { desc = "열린 버퍼 목록" })
  map("n", "<leader>fh", builtin.help_tags, { desc = "도움말 검색" })
  map("n", "<leader>fo", builtin.oldfiles, { desc = "최근 열었던 파일 목록" })
  map("n", "<leader>fw", builtin.grep_string, { desc = "커서 아래 단어로 검색" })
end

-- --------------------------------------------------------------------------
-- Telescope 키맵 (지연 로딩 대응 - 플러그인 로드 전에도 동작)
-- --------------------------------------------------------------------------
-- 위의 pcall이 실패할 경우를 대비한 문자열 기반 매핑
if not telescope_ok then
  map("n", "<leader>ff", "<cmd>Telescope find_files<CR>", { desc = "파일 이름으로 검색" })
  map("n", "<leader>fg", "<cmd>Telescope live_grep<CR>", { desc = "파일 내용(텍스트)으로 검색" })
  map("n", "<leader>fb", "<cmd>Telescope buffers<CR>", { desc = "열린 버퍼 목록" })
  map("n", "<leader>fh", "<cmd>Telescope help_tags<CR>", { desc = "도움말 검색" })
  map("n", "<leader>fo", "<cmd>Telescope oldfiles<CR>", { desc = "최근 열었던 파일 목록" })
  map("n", "<leader>fw", "<cmd>Telescope grep_string<CR>", { desc = "커서 아래 단어로 검색" })
end

-- --------------------------------------------------------------------------
-- q 키 비활성화 (기존 .vimrc와 동일)
-- --------------------------------------------------------------------------
-- 매크로 녹화를 실수로 시작하는 것 방지
map("n", "q", "<Nop>", opts)

