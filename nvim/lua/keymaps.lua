-- --------------------------------------------------------------------------
-- 주요 LSP 키맵은 lsp.lua의 on_attach에서 버퍼별로 설정됩니다.
-- 여기에는 참고용으로만 기술합니다:
--   gd         → 정의로 이동 (Go to Definition)
--   gr         → 참조 찾기 (Go to References)
--   K          → 호버 정보 (타입, 문서)
--   <leader>rn → 이름 변경 (Rename)
--   <leader>ca → 코드 액션 (Code Action)
--   [d / ]d    → 이전/다음 진단(에러/경고) 이동
